#include "libft.h"

char	*ft_strdiff(char *s1, char *s2)
{
	while (*s1 != '\0' || *s2 != '\0')
	{
		if (*s1 == *s2)
		{
			s1 = s1 + 1;
			s2 = s2 + 1;
		}
		else
			return (s1);
	}
	return (0);
}
