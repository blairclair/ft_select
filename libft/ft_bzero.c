#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*zer;
	size_t	i;

	i = 0;
	zer = (char*)s;
	while (i < n)
	{
		*zer = 0;
		zer = zer + 1;
		i++;
	}
}
